# ---------------------------------------------------------------------------
# VERIFICATION TOKEN
# ---------------------------------------------------------------------------

def create_verification_token(
    dataset_id: str,
    version_id: str,
    project_id: str,
    generated_by: str,
    supabase_client,
) -> str:
    """Generate a PLX-VRF-YYYY-XXXXX token and insert into verification_tokens."""
    token = _generate_token_string()

    expires_at = (datetime.now(timezone.utc) + timedelta(days=365)).isoformat()

    supabase_client.table("verification_tokens").insert({
        "resource_type": "dataset_lineage",
        "resource_id": version_id,
        "project_id": project_id,
        "token": token,
        "access_level": "summary",
        "created_by": generated_by,
        "expires_at": expires_at,
        "view_count": 0,
    }).execute()

    return token


def _generate_token_string() -> str:
    year = datetime.now(timezone.utc).year
    chars = string.ascii_uppercase + string.digits
    random_part = "".join(random.choices(chars, k=5))
    return f"PLX-VRF-{year}-{random_part}"


