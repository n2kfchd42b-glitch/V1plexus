# ---------------------------------------------------------------------------

async def generate_output_package(
    package_id: str,
    project_id: str,
    dataset_id: str,
    version_id: str,
    include_components: List[str],
    guideline: str,
    generated_by: str,
    supabase_client,
) -> None:
    """
    Background task: builds the .zip package, uploads to Supabase Storage,
    updates output_packages status to 'ready'.
    """
    try:
        generated_at = datetime.now(timezone.utc).isoformat()

        # Fetch needed data
        version_resp = supabase_client.table("dataset_versions").select("*").eq("id", version_id).single().execute()
        version = version_resp.data or {}

        quality_resp = (
            supabase_client.table("data_quality_reports")
            .select("*")
            .eq("version_id", version_id)
            .order("created_at", desc=True)
            .limit(1)
            .execute()
        )
        quality_report = quality_resp.data[0] if quality_resp.data else None

        audit_resp = (
            supabase_client.table("audit_logs")
            .select("*")
            .eq("resource_id", dataset_id)
            .order("created_at", desc=False)
            .execute()
        )
        audit_entries = audit_resp.data or []

        checklist_resp = (
            supabase_client.table("reporting_checklists")
            .select("*")
            .eq("version_id", version_id)
            .eq("guideline", guideline)
            .order("created_at", desc=True)
            .limit(1)
            .execute()
        )
        checklist = checklist_resp.data[0] if checklist_resp.data else None

        approval_resp = (
            supabase_client.table("approval_gates")
            .select("*")
            .eq("project_id", project_id)
            .eq("status", "approved")
            .limit(1)
            .execute()
        )
        approval = approval_resp.data[0] if approval_resp.data else None

        reentry_resp = (
            supabase_client.table("reentry_sessions")
            .select("*")
            .eq("project_id", project_id)
            .eq("status", "validated")
            .limit(1)
            .execute()
        )
        reentry_session = reentry_resp.data[0] if reentry_resp.data else None

        # Generate verification token
        token = create_verification_token(dataset_id, version_id, project_id, generated_by, supabase_client)

        # Build zip in memory
        zip_buffer = io.BytesIO()
        manifest_files: Dict[str, str] = {}

        with zipfile.ZipFile(zip_buffer, "w", zipfile.ZIP_DEFLATED) as zf:

            if "certificate" in include_components:
                content = generate_certificate_content(
                    project_id, dataset_id, version_id, "computing...", generated_at
                )
                filename = "certificate.html"
                zf.writestr(filename, content)
                manifest_files["certificate"] = filename

            if "checklist" in include_components and checklist:
                content = generate_checklist_content(
                    checklist.get("guideline", guideline),
                    checklist.get("items", {}),
                    checklist.get("total_items", 0),
                    checklist.get("auto_populated", 0),
                    checklist.get("manually_completed", 0),
                )
                filename = "reporting_checklist.html"
                zf.writestr(filename, content)
                manifest_files["checklist"] = filename

            if "quality_report" in include_components:
                content = generate_quality_pdf_content(quality_report)
                filename = "data_quality_report.html"
                zf.writestr(filename, content)
                manifest_files["quality_report"] = filename

            if "audit_summary" in include_components:
                content = generate_audit_summary_content(audit_entries)
                filename = "audit_trail_summary.html"
                zf.writestr(filename, content)
                manifest_files["audit_summary"] = filename

            if "approval" in include_components:
                content = generate_approval_content(approval)
                filename = "supervisor_approval.html"
                zf.writestr(filename, content)
                manifest_files["approval"] = filename

            if "reentry" in include_components:
                content = generate_reentry_content(reentry_session)
                filename = "reentry_validation.html"
                zf.writestr(filename, content)
                manifest_files["reentry"] = filename

            if "verification" in include_components:
                content = generate_verification_content(token, "pending")
                filename = "verification_info.html"
                zf.writestr(filename, content)
                manifest_files["verification"] = filename

        zip_bytes = zip_buffer.getvalue()

        # Compute SHA-256
        package_hash = hashlib.sha256(zip_bytes).hexdigest()

        # Update certificate with real hash
        if "certificate" in include_components:
            zip_buffer2 = io.BytesIO()
            with zipfile.ZipFile(io.BytesIO(zip_bytes), "r") as zf_read:
                with zipfile.ZipFile(zip_buffer2, "w", zipfile.ZIP_DEFLATED) as zf_write:
                    for item in zf_read.infolist():
                        data = zf_read.read(item.filename)
                        if item.filename == "certificate.html":
                            content = generate_certificate_content(
                                project_id, dataset_id, version_id, package_hash, generated_at
                            )
                            zf_write.writestr(item.filename, content)
                        else:
                            zf_write.writestr(item, data)
            zip_bytes = zip_buffer2.getvalue()
            package_hash = hashlib.sha256(zip_bytes).hexdigest()

        # Upload to Supabase Storage
        storage_path = f"packages/{project_id}/{package_id}.zip"
        try:
            supabase_client.storage.from_("research-packages").upload(
                storage_path,
                zip_bytes,
                {"content-type": "application/zip"},
            )
        except Exception:
            # Bucket may not exist yet; update status to failed in that case
            supabase_client.table("output_packages").update({
                "status": "failed",
                "package_hash": package_hash,
                "manifest": {"files": manifest_files, "error": "storage_upload_failed"},
            }).eq("id", package_id).execute()
            return

        # Update package record
        supabase_client.table("output_packages").update({
            "status": "ready",
            "package_hash": package_hash,
            "storage_path": storage_path,
            "manifest": {
                "files": manifest_files,
                "components": include_components,
                "guideline": guideline,
                "verification_token": token,
            },
        }).eq("id", package_id).execute()

        # Write audit entry
        supabase_client.table("audit_logs").insert({
            "actor_id": generated_by,
            "action": "dataset.package.generated",
            "resource_type": "dataset",
            "resource_id": dataset_id,
            "project_id": project_id,
            "details": {
                "summary": f"Output package generated: {len(include_components)} components",
                "operation": {
                    "package_id": package_id,
                    "components": include_components,
                    "guideline": guideline,
                    "package_hash": package_hash,
                    "storage_path": storage_path,
                },
            },
        }).execute()

    except Exception as exc:
        # Mark as failed
        try:
            supabase_client.table("output_packages").update({
                "status": "failed",
                "manifest": {"error": str(exc)},
            }).eq("id", package_id).execute()
        except Exception:
            pass
