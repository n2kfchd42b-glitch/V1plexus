def generate_quality_pdf_content(quality_report: Optional[Dict]) -> str:
    if not quality_report:
        return """<!DOCTYPE html><html><body>
<h1>Data Quality Report</h1>
<p>No quality report was available for this dataset version.</p>
</body></html>"""

    score = quality_report.get("overall_score") or quality_report.get("dqi_score") or "N/A"
    if isinstance(score, float) and score <= 1:
        score = f"{round(score * 100, 1)}%"
    status = quality_report.get("readiness_status") or quality_report.get("status") or "Unknown"

    return f"""<!DOCTYPE html>
<html>
<head><meta charset="utf-8"><title>Data Quality Report</title></head>
<body style="font-family: sans-serif; max-width: 800px; margin: 40px auto; padding: 20px;">
<h1>PLEXUS Data Quality Report</h1>
<p><strong>DQI Score:</strong> {score}</p>
<p><strong>Readiness Status:</strong> {status}</p>
<p><em>Full quality report exported from PLEXUS Data Quality Index v1.0 (Abrokwa, 2026).</em></p>
</body>
</html>"""


